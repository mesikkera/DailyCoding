#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << "== Find the absolute value == " << endl;
    cout << "abs(-10)   : " << abs(-10) << endl;
    cout << "abs(-5.72) : " << fabs(-5.72) << endl;

    cout << "== Find the square power == " << endl;
    cout << "pow(2, 2) : " << pow(2, 2) << endl;
    cout << "pow(3, 4) : " << pow(3, 4) << endl;
    cout << "pow(4, 6) : " << pow(4, 6) << endl;
    cout << "pow(8, 3) : " << pow(8, 3) << endl;

    return 0;
}